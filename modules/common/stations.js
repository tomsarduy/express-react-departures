module.exports.getStation  = (code) => {

	// Validate the station code
	if(!code || typeof(code) !== 'string' || code.length!==3){
		return false;
	}

	const crs = code.toUpperCase();
	const station = this.stationsList.find((station) => station.value === crs);

	// If the station doesn't exist
	if(!station){
		return false;
	}

	// Return the station Object
	return {
		crs,
		name: station.label
	}
}

module.exports.getStationName  = (code) => {

	const station = this.getStation(code);

	if(!station || typeof(station) !== 'object'){
		return 'N/A';
	}

	return station.name

}